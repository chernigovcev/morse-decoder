const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    
    let text = '';
    let char = '';
    let a = 0;

    for (var i = 0; (expr.length / 10) > i; i++) {

        for (var e = 0; 5 > e; e++) {
            if (expr[a] == '*') {
                text += ' ';
                a += 10;
                i++;
            } else if (expr[a] + expr[a + 1] == '10') {
                char += '.';
            } else if (expr[a] + expr[a + 1] == '11') {
                char += '-';
            }
            a += 2;
        }
        text += MORSE_TABLE[char];
        char = '';
    } 
    return text;
}

module.exports = {
    decode
}